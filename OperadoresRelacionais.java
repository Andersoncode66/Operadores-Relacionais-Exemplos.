public class OperadoresRelacionais {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        // 6. Operador <= (Menor ou igual a)


        if (a <= b) {
            System.out.println("a é menor ou igual a b");
        } else {
            System.out.println("a não é menor ou igual a b");
        }
    }
}
