public class OperadoresDiferente {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        // 2. Operador != (Diferente de)

        if (a != b) {
            System.out.println("a é diferente de b");
        } else {
            System.out.println("a não é diferente de b");
        }
    }
}
